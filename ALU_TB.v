`timescale 1ns / 1ps

module ALU_16BIT_tb;

reg [15:0] A;
reg [15:0] B;
reg [2:0] OPCODE;
reg [1:0] LOGIC_SEL;

wire [15:0] RESULT;
wire CARRY;
wire BORROW;
wire SIGN;
wire ZERO;
wire PARITY;
wire OVERFLOW;
wire AgtB;
wire AltB;
wire AeqB;

// Instantiate ALU
ALU_16BIT DUT(
    .RESULT(RESULT),
    .CARRY(CARRY),
    .BORROW(BORROW),
    .SIGN(SIGN),
    .ZERO(ZERO),
    .PARITY(PARITY),
    .OVERFLOW(OVERFLOW),
    .AgtB(AgtB),
    .AltB(AltB),
    .AeqB(AeqB),
    .A(A),
    .B(B),
    .OPCODE(OPCODE),
    .LOGIC_SEL(LOGIC_SEL)
);

initial
begin

$monitor("TIME=%0t OPCODE=%b LOGIC_SEL=%b A=%h B=%h RESULT=%h CARRY=%b BORROW=%b SIGN=%b ZERO=%b PARITY=%b OVERFLOW=%b GT=%b LT=%b EQ=%b",
$time, OPCODE, LOGIC_SEL, A, B, RESULT,
CARRY, BORROW, SIGN, ZERO, PARITY,
OVERFLOW, AgtB, AltB, AeqB);

//////////////////////////////////////////////////
// ADD
//////////////////////////////////////////////////

A = 16'h1234;
B = 16'h4321;
OPCODE = 3'b000;
LOGIC_SEL = 2'b00;
#10;

//////////////////////////////////////////////////
// SUBTRACT
//////////////////////////////////////////////////

A = 16'h5678;
B = 16'h1234;
OPCODE = 3'b001;
#10;

//////////////////////////////////////////////////
// LOGIC AND
//////////////////////////////////////////////////

A = 16'hF0F0;
B = 16'h0FF0;
OPCODE = 3'b010;
LOGIC_SEL = 2'b00;
#10;

//////////////////////////////////////////////////
// LOGIC OR
//////////////////////////////////////////////////

LOGIC_SEL = 2'b01;
#10;

//////////////////////////////////////////////////
// LOGIC XOR
//////////////////////////////////////////////////

LOGIC_SEL = 2'b10;
#10;

//////////////////////////////////////////////////
// LOGIC NOT
//////////////////////////////////////////////////

LOGIC_SEL = 2'b11;
#10;

//////////////////////////////////////////////////
// INCREMENT
//////////////////////////////////////////////////

A = 16'h00FF;
OPCODE = 3'b011;
#10;

//////////////////////////////////////////////////
// DECREMENT
//////////////////////////////////////////////////

A = 16'h0100;
OPCODE = 3'b100;
#10;

//////////////////////////////////////////////////
// COMPARE (A>B)
//////////////////////////////////////////////////

A = 16'h5678;
B = 16'h1234;
OPCODE = 3'b101;
#10;

//////////////////////////////////////////////////
// COMPARE (A<B)
//////////////////////////////////////////////////

A = 16'h1234;
B = 16'h5678;
#10;

//////////////////////////////////////////////////
// COMPARE (A=B)
//////////////////////////////////////////////////

A = 16'hAAAA;
B = 16'hAAAA;
#10;

$finish;

end

endmodule